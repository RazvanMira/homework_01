print("Introduce two integers.")
a = int(input("a="))
b = int(input("b="))

sum = a + b
print("The sum of a and b is " + str(sum))

difference = a - b
print("The difference when b is subtracted from a is " + str(difference))

product = a * b
print("The product of a and b is " + str(product))

quotient = a / b
print("The quotient when a is divided by b " + str(quotient))

remainder = a // b
print("The remainder when a is divided by b is " + str(remainder))

power = a**b
print("The result of a to the power b is " + str(power))